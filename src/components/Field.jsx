class Field extends Component {
  state = { fild: startF };

  renderCells = () => {
    return this.state.fild.map((row, y) => {
      return row.map((cell, x) => {
        return <div key={`${x}${y}`} className={cn({'bg-secondary': (cell === 1), "border": true})} />
      })
    })
  };

  nextState = () => {
    this.setState({ fild: step(this.state.fild) });
  }

  render() {
    const style = {
      display: 'grid',
      width: '500px',
      height: '500px',
      gridTemplateRows: `repeat(${this.props.size}, 1fr)`,
      gridTemplateColumns: `repeat(${this.props.size}, 1fr)`,
    };
    return (
      <div className="d-flex justify-content-center m-5" onClick={this.nextState}>
        <div style={style} className="border">
          {this.renderCells()}
        </div>
      </div> 
    );
  }
}

